// Constructor
ThreadFuncs::ThreadFuncs() : threadDone(threadNum) {

  // Initialise thread completion flags
  for (auto& flag : threadDone) {
    // Initialise as false
    flag.store(false);                                                    
  }

}

// Pin thread to specific CPU core
void ThreadFuncs::pin_thread_to_core(size_t core_id) {

  // Define CPU set
  cpu_set_t cpuset;
  // Clear CPU set
  CPU_ZERO(&cpuset);
  // Add specified core to set 
  CPU_SET(core_id, &cpuset);
  // Set thread affinity
  pthread_setaffinity_np(pthread_self(), sizeof(cpu_set_t), &cpuset);
  
}

