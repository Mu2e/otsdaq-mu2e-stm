// General worker function for processing stages
void ThreadFuncs::workerThread(RingBuffer<DataStruct>& inputQueue,
			       RingBuffer<DataStruct>& outputQueue,
			       BufferPool& pool,
			       const char* stage,
			       std::atomic<uint64_t>& totalBytesProcessed,
			       size_t core_id,
			       std::function<void(std::shared_ptr<DataStruct>&)> operation,
			       size_t threadIndex) {


  // Bind thread to specific core
  pin_thread_to_core(core_id);

  bool first = true;
  
  // Start timing
  auto start_time = std::chrono::high_resolution_clock::now();
  
  // Continue until first thread is done, input queue is empty
  // and previous thread is done
  while (!threadDone[0].load() || !inputQueue.is_empty() || !threadDone[threadIndex - 1].load()) {

    // Retrieve a buffer from the input queue
    auto buffer = inputQueue.pop();

    // If buffer is valid
    if (buffer) {

      // Restart timer
      if (first){
	start_time = std::chrono::high_resolution_clock::now(); 
	first = false;
      }            

      // Perform the operation on the buffer
      operation(buffer); 

      // Append stage-specific metadata
      strncat(buffer->metadata, stage, sizeof(buffer->metadata) - strlen(buffer->metadata) - 1);

      // Calculate bytes processed 
      uint64_t bytesProcessed = buffer->size * sizeof(int16_t);
      // Update total bytes processed 
      totalBytesProcessed += bytesProcessed;

      // Push the processed buffer to the next stage
      outputQueue.push(buffer);
      
    }
    
  }

  // End timing
  auto end_time = std::chrono::high_resolution_clock::now();

  // Signal completion to the next thread
  threadDone[threadIndex].store(true);

  // Calculate elapsed time 
  std::chrono::duration<double> elapsed = end_time - start_time;
  // Convert bytes to Gbits
  double gbytes = totalBytesProcessed.load() / 1e9;
  double gbits = gbytes * 8.0; // Convert to Gbits
  // Print processing speed
  std::cout << "[" << stage << "] Processed "
	    << std::fixed << std::setprecision(2)
	    << (gbytes) << " Gbytes at\n"
      	    << std::fixed << std::setprecision(2)
	    << (gbits / elapsed.count()) << " Gbit/s\n";

}


